-- 5.1 текущая база данных, пользователь и дата
select
    database() as `текущая база`,
    user() as `пользователь`,
    curdate() as `текущая дата`,
    curtime() as `текущее время`,
    now() as `дата и время`;

-- 5.2 заказы текущего года с датой, днем и месяцем
select
    zak.`Код_заказа` as `номер заказа`,
    zak.`Дата_заказа` as `дата`,
    day(zak.`Дата_заказа`) as `день`,
    monthname(zak.`Дата_заказа`) as `месяц`
from `Заказы` as zak
where year(zak.`Дата_заказа`) = year(curdate());

-- 5.3 замена двойных пробелов и округление цены до десятков
update `Книги`
set
    `Название` = replace(`Название`, '  ', ' '),
    `Цена` = round(`Цена`, -1);

-- 5.4 автор, книга и цена с денежной единицей
select
    concat_ws(' ', a.`Фамилия`, a.`Имя`) as `автор`,
    concat_ws(' ', k.`Название`, concat(k.`Цена`, ' руб.')) as `книга и цена`
from `Авторы` as a
inner join `Книги` as k on k.`Код_автора` = a.`Код_автора`;

-- 5.5 книги каждого автора без повторов по возрастанию
select
    concat_ws(' ', a.`Фамилия`, a.`Имя`) as `автор`,
    group_concat(distinct k.`Название` order by k.`Название` separator ', ') as `книги`
from `Авторы` as a
left join `Книги` as k on k.`Код_автора` = a.`Код_автора`
group by a.`Код_автора`, a.`Фамилия`, a.`Имя`;

-- 5.6 информация о заказчике с заменой пустых значений
select
    z.`Логин` as `логин`,
    ifnull(z.`Телефон`, '-') as `телефон`,
    ifnull(z.`Адрес`, '-') as `адрес`
from `Заказчики` as z;

-- 5.7 нумерация книг по названию
select
    row_number() over (order by k.`Название`) as `номер`,
    k.*
from `Книги` as k
order by k.`Название`;

-- 5.8 топ три цены внутри каждого жанра
select *
from (
    select
        k.*,
        dense_rank() over (partition by k.`Жанр` order by k.`Цена` desc) as `место`
    from `Книги` as k
) as t
where t.`место` <= 3
order by t.`Жанр`, t.`место`, t.`Цена` desc;

