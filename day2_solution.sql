-- 5.1 создание и заполнение временной таблицы с книгами
create temporary table tempBooks (
    `автор` varchar(255),
    `книга` varchar(255),
    `цена` decimal(10,2)
);

insert into tempBooks (`автор`, `книга`, `цена`)
select
    concat(a.`Фамилия`, ' ', a.`Имя`) as `автор`,
    k.`Название` as `книга`,
    k.`Цена` as `цена`
from `Авторы` as a
inner join `Книги` as k on k.`Код_автора` = a.`Код_автора`;

select * from tempBooks;

-- 5.2 удаление книг, где название содержит слово компьютер
 delete from tempBooks
where lower(`книга`) like '%компьютер%';

select * from tempBooks;

-- 5.3 изменение цены в tempBooks через case
update tempBooks
set `цена` = case
    when lower(`автор`) like '%пушкин%' then `цена` * 2
    when lower(`автор`) like '%иванов%' then `цена` - 50
    else `цена`
end;

select * from tempBooks;

-- 5.4 удаление tempBooks
 drop temporary table if exists tempBooks;

-- 5.5 увеличение цены всех книг русских авторов на сто рублей
update `Книги` as k
inner join `Авторы` as a on a.`Код_автора` = k.`Код_автора`
set k.`Цена` = k.`Цена` + 100
where a.`Страна` = 'Россия';

-- 5.6 удаление заказчиков без заказов
 delete from `Заказчики`
where `Код_заказчика` not in (
    select t.`Код_заказчика`
    from (
        select distinct `Код_заказчика`
