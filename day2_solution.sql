-- 5.1 книги русских авторов
select
    concat(a.`Фамилия`, ' ', a.`Имя`) as `автор`,
    k.`Название` as `книга`,
    k.`Цена` as `цена`
from `Авторы` as a
inner join `Книги` as k on k.`Код_автора` = a.`Код_автора`
where a.`Страна` = 'Россия';

-- 5.2 количество книг каждого автора
select
    concat(a.`Фамилия`, ' ', a.`Имя`) as `автор`,
    count(k.`Код_книги`) as `количество книг`
from `Авторы` as a
left join `Книги` as k on k.`Код_автора` = a.`Код_автора`
group by a.`Код_автора`, a.`Фамилия`, a.`Имя`
order by a.`Фамилия`, a.`Имя`;

-- 5.3 авторы, которые не писали книги с linux или windows в названии
select
    a.`Имя`,
    a.`Фамилия`
from `Авторы` as a
where a.`Код_автора` not in (
    select k.`Код_автора`
    from `Книги` as k
    where lower(k.`Название`) like '%linux%'
       or lower(k.`Название`) like '%windows%'
);

-- 5.4 статистика по каждому заказчику
select
    z.`Логин` as `логин`,
    count(distinct zak.`Код_заказа`) as `количество заказов`,
    coalesce(sum(sz.`Количество`), 0) as `количество купленных книг`,
    coalesce(sum(sz.`Количество` * k.`Цена`), 0) as `сумма`
from `Заказчики` as z
left join `Заказы` as zak on zak.`Код_заказчика` = z.`Код_заказчика`
left join `Состав_заказа` as sz on sz.`Код_заказа` = zak.`Код_заказа`
left join `Книги` as k on k.`Код_книги` = sz.`Код_книги`
group by z.`Код_заказчика`, z.`Логин`;

-- 5.5 заказчики, купившие больше десяти книг
select
    z.`Логин` as `логин`,
    count(distinct zak.`Код_заказа`) as `количество заказов`,
    coalesce(sum(sz.`Количество`), 0) as `количество купленных книг`
from `Заказчики` as z
left join `Заказы` as zak on zak.`Код_заказчика` = z.`Код_заказчика`
left join `Состав_заказа` as sz on sz.`Код_заказа` = zak.`Код_заказа`
group by z.`Код_заказчика`, z.`Логин`
having coalesce(sum(sz.`Количество`), 0) > 10;

